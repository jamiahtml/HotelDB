    public ArrayList<Bill> billList(){
        ArrayList<Bill> billList = new ArrayList();
        try{
            con = Connections.getConnection();
            String query1 = "SELECT * FROM bill WHERE guest_username='"+user+"'";
            Statement st = con.createStatement();
            rs = st.executeQuery(query1);
            Bill bill;
            while (rs.next()){
                bill = new Bill(rs.getString("res_num"), rs.getDouble("payment_amount"), rs.getString("cc_info"),rs.getString("paid_on"));
                billList.add(bill);
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
        return billList;
    }
    public void show_bill(){
        ArrayList<Bill> list = billList();
        DefaultTableModel model = (DefaultTableModel)jTable_Display_Bills.getModel();
        Object[] row = new Object[4];
        for (int i = 0; i < list.size();i++){
            row[0] = list.get(i).getResNum();
            row[1] = list.get(i).getPayDate();
            row[2] = list.get(i).getAmount();
            row[3] = list.get(i).getCC();
            model.addRow(row);
            
        }
    }

