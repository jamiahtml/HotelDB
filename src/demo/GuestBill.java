    private void UpdateCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateCCActionPerformed
        // TODO add your handling code here:
        try{
            con = Connections.getConnection();
            String query = "UPDATE guest SET cc_info = ? WHERE g_username ='"+user+"'";
            String query2 = "UPDATE bill SET cc_info = ? WHERE guest_username ='"+user+"'";
            pts = con.prepareStatement(query);
            PreparedStatement billInfo = con.prepareStatement(query2);
            pts.setString(1, cc_info.getText());
            billInfo.setString(1,cc_info.getText());
            pts.executeUpdate();
            billInfo.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)jTable_Display_Bills.getModel();
            model.setRowCount(0);
            show_bill();
            JOptionPane.showMessageDialog(null, "Update Guest CC_Info and Bill Information!");
            
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_UpdateCCActionPerformed

